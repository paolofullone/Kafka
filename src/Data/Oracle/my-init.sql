ALTER SESSION SET CONTAINER=XEPDB1;

CREATE USER PAOLO IDENTIFIED BY password DEFAULT TABLESPACE users QUOTA UNLIMITED ON users;

GRANT CONNECT, RESOURCE TO PAOLO;

CREATE TABLE SAMPLE_MESSAGE (
    Id NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY, 
    MESSAGE_ID RAW(16) DEFAULT SYS_GUID() NOT NULL,
    MESSAGE_DATE TIMESTAMP DEFAULT CURRENT_TIMESTAMP NOT NULL,
    MESSAGE_DELETED_DATE TIMESTAMP DEFAULT NULL
);

CREATE TABLE WO_ACCESS (
    Id NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY, 
    MESSAGE_ID RAW(16) DEFAULT SYS_GUID() NOT NULL,
    MESSAGE_DATE TIMESTAMP DEFAULT CURRENT_TIMESTAMP NOT NULL,
    MESSAGE_DELETED_DATE TIMESTAMP DEFAULT NULL
);

GRANT SELECT, INSERT, UPDATE, DELETE ON SAMPLE_MESSAGE TO PAOLO;

COMMIT;

SELECT * FROM SAMPLE_MESSAGE;

SELECT * FROM WO_ACCESS;